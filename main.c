#include <stdio.h>
#include "get_next_line.h"

int	main(int argc, char **argv)
{
	int		i;
	char	*str;
	int		fd;

	i = 0;
	if (argc != 2)
		return (1);
	fd = open(argv[1], O_RDONLY);
	str = get_next_line(fd);
	while (str)
	{
		printf("%s", str);
		free(str);
		str = get_next_line(fd);
	}
	free(str);
	return (0);
}
